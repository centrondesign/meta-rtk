u16 findRatio8(u64 delta_phase, u64 *min_diff)
{
	*min_diff = 0x7fffffff;
	u16 idx = 0;
	u16 i;

	if (delta_phase < 0x4000) { /* upscaling */
		*min_diff = 0;
		idx = 0;
		return idx;
	}

	for (i = 1; i < sizeof(VO_ScaleRatio8) / sizeof(u32); i++) {
		/*
	* VscaleRatio8 is incrementing, we find the first one that's bigger than or equal to delta
	* instead of min ads diff. This way it will round up to lower pass filter to avoid alias
	*/
		if (VO_ScaleRatio8[i] >= delta_phase)
			break;
	}
	if (i >= (sizeof(VO_ScaleRatio8) / sizeof(u32)))
		i = sizeof(VO_ScaleRatio8) / sizeof(u32) - 1;
	*min_diff = abs((s32)VO_ScaleRatio8[i] - (s32)delta_phase);
	idx = i;
	return idx;
}

/* form using stretch to achive semiplanner -> planner convert */
void SetCoeffsCbCrMode(u16 *coeff, u32 cbMode, u32 taps, u32 init_phase)
{
	u16 i;
	const u16 *p;

	static const u16 cb_coef_8t16p[8][8] = {
		{ 0, 0, 0, 256, 0, 0, 0, 0 }, { 0, 0, 0, 256, 0, 0, 0, 0 },
		{ 0, 0, 0, 256, 0, 0, 0, 0 }, { 0, 0, 0, 256, 0, 0, 0, 0 },
		{ 0, 0, 0, 256, 0, 0, 0, 0 }, { 0, 0, 0, 256, 0, 0, 0, 0 },
		{ 0, 0, 0, 256, 0, 0, 0, 0 }, { 0, 0, 0, 256, 0, 0, 0, 0 }
	};

	static const u16 cr_coef_8t16p[8][8] = {
		{ 0, 0, 0, 0, 256, 0, 0, 0 }, { 0, 0, 0, 0, 256, 0, 0, 0 },
		{ 0, 0, 0, 0, 256, 0, 0, 0 }, { 0, 0, 0, 0, 256, 0, 0, 0 },
		{ 0, 0, 0, 0, 256, 0, 0, 0 }, { 0, 0, 0, 0, 256, 0, 0, 0 },
		{ 0, 0, 0, 0, 256, 0, 0, 0 }, { 0, 0, 0, 0, 256, 0, 0, 0 }
	};

	if (cbMode) {
		p = cb_coef_8t16p[0];
	} else {
		p = cr_coef_8t16p[0];
	}
	for (i = 0; i < (taps << 3); i++) {
		u16 x = i & 15;
		u16 y = i >> 4;

		coeff[i] = (x < 8 ? p[x * taps + taps - 1 - y] :
				    p[(15 - x) * taps + y])
			   << 4;
	}
}

void SetVideoScalingCoeffs(u16 *coeff, u32 delta_phase, u32 init_phase,
			   u32 taps, u32 swap)
{
	u16 i;

	if (abs(delta_phase - 0x4000) < 0x20 && init_phase == 0) {
		memset(coeff, 0, taps << 4);
		for (i = 1; i <= 8; i++)
			coeff[(taps << 3) - i] = 0x1000;
	} else if (taps == 2) {
		for (i = 0; i < 16; i += 2) {
			u16 c = VO_coef_2t8p[i >> 1];
			coeff[i] = c;
			coeff[i + 1] = c;
		}
	} else {
		const u16 *p;
		u64 min_diff;
		u16 idx = findRatio8(delta_phase, &min_diff);

		p = (taps == 4 ? VO_coef_4t16p_ratio[idx][0] :
				 VO_coef_8t16p_ratio[idx][0]);
		for (i = 0; i < (taps << 3); i++) {
			u16 x = i & 15;
			u16 y = i >> 4;

			coeff[i] = (x < 8 ? p[x * taps + taps - 1 - y] :
					    p[(15 - x) * taps + y])
				   << 4;
		}
	}
}
